//  Function Name : FhtoCs
//  Description   : It is used to convert fahrenheit in to celsius 
//  Input         : Intiger  
//  OutPut        : Intiger  
//  Author        : Pratibha Sagar Pawar
//  Date          : 29 Oct 2025
