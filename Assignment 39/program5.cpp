void Display()
{
    static char ch = 'a';

    if(ch<='f')
    {
        cout<<ch<<"\t";
        ch++;
        Display();
    }
}
////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    Display();
    return 0;
}
