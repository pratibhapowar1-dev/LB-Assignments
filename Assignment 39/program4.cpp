void Display()
{
    static char ch = 'A';

    if(ch<='F')
    {
        cout<<ch<<"\t";
        ch++;
        Display();
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    Display();
    return 0;
}
