template <class T>
T Swap(T no1, T no2)
{
    T temp;

    temp = no1;
    no1 = no2;
    no2 = temp;
}            

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
   int a = 10, b = 20;
   swap(a,b);
   cout<<"After Swap : a= "<< a <<"b = "<< b <<"\n";

   float x = 3.5f, y = 7.8f;
   swap(x,y);
   cout<<"After Swap : x = "<< a<<"y = "<< b <<"\n";

   return 0;
}
