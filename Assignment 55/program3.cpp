////////////////////////////////////////////////////////////////////////////////

template <class T>
T Div(T no1, T no2)
{
    T Ans = no1/no2;
    return Ans;
}            

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    float fRet = 0;

    iRet = Div(30,10);
    cout<< "Substraction of integer is : "<< iRet << "\n";
    
    fRet = Div(30.5f,10.5f);
    cout<< "Substraction of float is: "<< fRet << "\n";
    
    return 0;
}
