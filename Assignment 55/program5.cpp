#include<iostream>               // For Input Output
using namespace std;             

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Max
//  Description   : It is used to find maximum number among four values
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////

template <class T>
T Max(T a, T b, T c, T d)
{
    T max = a;

    if(b > max)
    {
        max = b;
    }
    if(c > max)
    {
        max = c;
    }
    if(d > max)
    {
        max = d;
    }
    return max;
}           

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iMax = Max(10, 40, 30, 25);
    cout << "Maximum of four (int)   : " << iMax << "\n";

    float fMax = Max(10.5f, 40.2f, 30.1f, 25.9f);
    cout << "Maximum of four (float) : " << fMax << "\n";
    
    return 0;
}
