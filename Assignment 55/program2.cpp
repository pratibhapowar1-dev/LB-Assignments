#include<iostream>               // For Input Output
using namespace std;             

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sub
//  Description   : It is used to Substraction of two number
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////

template <class T>
T Sub(T no1, T no2)
{
    T Ans = no1-no2;
    return Ans;
}            

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    float fRet = 0;

    iRet = Sub(30,20);
    cout<< "Substraction of integer is : "<< iRet << "\n";
    
    fRet = Sub(30.5f,20.3f);
    cout<< "Substraction of float is: "<< fRet << "\n";
    
    return 0;
}
