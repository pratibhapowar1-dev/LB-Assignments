template <class T>
T Add(T no1, T no2)
{
    T Ans = no1+no2;
    return Ans;
}            

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    float fRet = 0;

    iRet = Add(10,20);
    cout<< "Addition of integer is : "<< iRet << "\n";
    
    fRet = Add(10.5f,20.3f);
    cout<< "Addition of float is: "<< fRet << "\n";
    
    return 0;
}
