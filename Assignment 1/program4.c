void DisplayConvert(char cValue)
{
    if(cValue>='A' && cValue<='Z')
    {
        cValue = cValue + 32;
        printf("%c",cValue );
    }
    else if(cValue>='a' && cValue<= 'z')
    {
        cValue = cValue - 32;
        printf("%c",cValue );
    }
    else
    {
        printf("%c",cValue);
    }
}
////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    char cValue = '\0';

    printf("Enter Character:\n");
    scanf("%c",&cValue);

    DisplayConvert(cValue);

    return 0;
}
