void PrintEven(
                int iNo
              )
{
    int iCnt = 0;
    if(iNo <= 0)
    {
        return;
    }
    
    for(iCnt = 1; iCnt<= iNo; iCnt++)
    {
        printf("%d\t",iCnt*2);
    
    }
    printf("\n");
}
