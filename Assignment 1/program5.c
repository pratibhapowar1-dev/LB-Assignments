typedef int bool;
#define TRUE 1
#define FALSE 0

bool Chkvowel(char cValue)
{
    if((cValue == 'a' )||(cValue == 'e' ) || (cValue == 'i' ) || (cValue == 'o' )|| (cValue == 'u' ))
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }

}
