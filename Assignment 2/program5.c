//  Function Name : FactDiff
//  Description   : It is used to return difference between summation of facters and nonfacters
//  Input         : Intiger
//  OutPut        : Intiger 
//  Author        : Pratibha Sagar Pawar
//  Date          : 29 Oct 2025
