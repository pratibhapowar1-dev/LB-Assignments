void print_facters(int number)
{
    if(number<=0)
    {
        printf("Invalid Number");
        return;
        
    }
    int iCnt = 0;
    for(iCnt = 1; iCnt <= number/2; iCnt++) //
    {
        if(number % iCnt == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}
