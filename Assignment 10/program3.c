int sum_of_factors(int number)
{
    int iCnt = 0, iSum = 0;

       if(number < 0)
    {
        number = -number;
    }

    for (iCnt = 1; iCnt <= number/2 ; iCnt++)
    {
        if (number % iCnt == 0)
        {
            iSum = iSum+iCnt;
        }
    }
    return iSum;
}
