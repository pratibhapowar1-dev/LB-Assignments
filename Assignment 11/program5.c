int sum_Even_numbers(int limit)
{
   int  iCnt = 0;
   int iSum = 0;

    for(iCnt=1; iCnt<=limit; iCnt++)
    {   
        if(iCnt%2==0)
        {
             iSum = iSum + iCnt;
        }
    }
    return iSum;
}
