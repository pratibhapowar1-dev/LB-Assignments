void Display(char ch)
{

    {
        if(ch  >= 'A' && ch <= 'Z')
        {
            printf("%c\n",ch + 32);
        }
        else if(ch >='a' && ch <= 'z')
        {
            printf("%c\n",ch - 32);

        }
        else
        {
            printf("%c\n",ch);
        }
    }

   

}

////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
