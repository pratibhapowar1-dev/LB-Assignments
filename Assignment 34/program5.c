void Display(char ch)
{
    printf("Desimal : %d\n",ch);
    printf("Octal : 0%o\n",ch);
    printf("Hexadecimal : OX%X\n",ch);

}

////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
   char cValue = '\0';

   printf("Enter the character : \n");
   scanf("%c",&cValue);

   Display(cValue);
  
   return 0;
}

