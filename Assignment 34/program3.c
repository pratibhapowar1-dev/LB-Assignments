void Display(char ch)
{
   if(ch >= 'A' && ch<= 'Z')
   {
    while(ch <='Z')
    {
        printf("%c",ch);
        ch++;
    }
   }
   else if(ch >= 'a' && ch<= 'z')
   {
    while(ch >='a')
    {
        printf("%c",ch);
        ch--;
    }
   }
    printf("\n");   

}

