void strtogglex(char str[])                          

{ 
   while(*str != '\0')
    {
      if(*str >= 'a' && *str <='z')
      {
        *str = *str - 32;
      }
      else if(*str >= 'A' && *str <='Z')
      {
        *str = *str + 32;
      }
      str++;
    }
   
}
