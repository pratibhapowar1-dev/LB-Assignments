int OddFactorial(int iNo)
{
    int iFact = 1;  
    if(iNo<0)
    {
        iNo = -iNo;
    }

    int iCnt = 0; 
    for(iCnt=1;iCnt<=iNo; iCnt++)
    {
        if(iCnt%2!=0)
        {
        iFact = iCnt*iFact;
        }
    }
    return iFact;
}
