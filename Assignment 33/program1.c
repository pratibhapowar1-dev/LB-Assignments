typedef int BOOL;

BOOL ChkAlpha(char ch)
{
    if(ch >= 'A' && ch <='Z'|| ch>='a'&& ch<='z')    
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }

}

////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    BOOL bRet = FALSE;

    printf("Enter the character : ");
    scanf("%c",&cValue);

    bRet = ChkAlpha(cValue);

    if(bRet == TRUE)
    {
        printf("It is Character");
    }
    else
    {
       printf("it is not a character");

    }
    return 0;
}
