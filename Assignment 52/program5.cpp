#include<iostream>               // For Input Output
using namespace std;             //
////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Min
//  Description   : It is used to find minimum element
//  Input         : Generic Array
//  Output        : Minimum value
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////
template <class T>
T Min(T *Arr, int iSize)
{
    T iMin = Arr[0];
    int i = 0;

    for(i = 1; i < iSize; i++)
    {
        if(Arr[i] < iMin)
        {
            iMin = Arr[i];
        }
    }  
    return iMin;
}            
////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    int Arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.0, 3.7, 9.8, 8.7};

    int iRet = Min(Arr, 5);
    cout << iRet << " ";

    float fRet = Min(brr, 4);
    cout << fRet << " ";

    return 0;
}
