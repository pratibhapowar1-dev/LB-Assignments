template <class T>
T Multiply(
                T iNo1 ,
                T iNo2
              )
{
    T ans;
    ans = iNo1*iNo2;
    return ans;
}              
////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
   
    int iRet = Multiply(10,20);
    cout<<iRet<<" ";

    float fRet = Multiply(10.0f , 20.0f);
    cout<<fRet<<" ";

    return 0;


}
