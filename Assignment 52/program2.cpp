template <class T>
T Max(
                T iNo1,
                T iNo2,
                T iNo3
              )
{
   T Max = iNo1;

   if(iNo2 > Max)
   {
    Max = iNo2;
   }
   if(iNo3>Max)
   {
    Max = iNo3;
   }
   return Max;

}              
