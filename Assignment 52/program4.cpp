#include<iostream>               // For Input Output
using namespace std;             //
////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Max
//  Description   : It is used to find maximum element
//  Input         : Generic Array
//  Output        : Maximum value
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////
template <class T>
T Max(T *Arr, int iSize)
{
    T iMax = Arr[0];
    int i = 0;

    for(i = 1; i < iSize; i++)
    {
        if(Arr[i] > iMax)
        {
            iMax = Arr[i];
        }
    }  
    return iMax;
}            
////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    int Arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.0, 3.7, 9.8, 8.7};

    int iRet = Max(Arr, 5);
    cout << iRet << " ";

    float fRet = Max(brr, 4);
    cout << fRet << " ";

    return 0;
}
