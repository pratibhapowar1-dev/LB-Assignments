using namespace std;             
////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckSorted
//  Description   : It is used to check whether array is sorted
//  Input         : Generic Array
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////

template <class T>
bool CheckSorted(T *Arr, int iSize)
{
    for(int i = 0; i < iSize - 1; i++)
    {
        if(Arr[i] > Arr[i+1])
        {
            return false;
        }
    }
    return true;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int Arr[] = {10, 20, 30, 40, 50};

    cout << "Is Sorted : ";
    if(CheckSorted(Arr,5))
        cout << "TRUE";
    else
        cout << "FALSE";

    return 0;
}
