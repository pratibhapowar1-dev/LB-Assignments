////////////////////////////////////////////////////////////////////////////////

template <class T>
void Replace( T *Arr, int iSize, T oldValue, T newValue)
{
   
    for(int i = 0; i < iSize; i++)
    {
        if(Arr[i] == oldValue)
        {
            Arr[i] = newValue;
        }
    }
}


////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int Arr[] = {10, 20, 30, 40, 50};
    Replace(Arr,5,30,99);

    cout<<"Array after Replace : ";
    for(int i = 0; i<5;i++)
    {
        cout<<Arr[i]<<" ";
    }
    return 0;
}
