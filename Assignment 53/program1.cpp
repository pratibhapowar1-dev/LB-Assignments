#include<iostream>               // For Input Output
using namespace std;             // 
////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CopyArray
//  Description   : It is used to copy one array into another array
//  Input         : Generic Array
//  Output        : copied Array
//  Author        : Pratibha Sagar Pawar
//  Date          : 22 Jan 2026
//      
////////////////////////////////////////////////////////////////////////////////

template <class T>
void CopyArray(T *src, T *dest, int iSize)
{
    int i = 0;
    for(i = 0; i < iSize; i++)
    {
      dest[i] = src[i];
    }  
   
}            
////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    int Arr[] = {10, 20, 30, 40, 50};
    int brr[5];

    CopyArray(Arr,brr,5);

    cout<<"Copied Array : ";
    for(int i = 0; i<5; i++)
    {
        cout<<brr[i]<<" ";
    }

    return 0;
}
