void Display(int iNo)
{
    static int iCnt = 1;
    if(iCnt<=iNo)
    {
        cout<<iCnt<<"\t";
        iCnt++;
        Display(iNo);
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    cout<<"Enter Number :";
    cin>>iValue;

    Display(iValue);

    return 0;
}
