int Max(int iNo)
{
    static int iMax = 0;

    if (iNo != 0)
    {
       int iDigit = iNo%10;

       if (iDigit > iMax)
       {
         iMax = iDigit;
       }
       Max(iNo / 10);
       
    }
    return iMax;
    
}

