int Small(char *str)
{
    static int iCount = 0;

    if (*str != '\0')
    {
        
        if ((*str >= 'a') &&  (*str <= 'z'))
        {
            iCount++;
        }
        Small(str+1);

    }
    return iCount;
    
}

