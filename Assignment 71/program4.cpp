int Min(int iNo)
{
    static int iMin = 0;

    if (iNo != 0)
    {
       int iDigit = iNo%10;

       if (iDigit < iMin)
       {
         iMin = iDigit;
       }
       Min(iNo / 10);
       
    }
    return iMin;
    
}

