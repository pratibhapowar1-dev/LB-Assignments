void StrCpySmall(char *src, char *dest)
{
    while(*src != '\0')
    {
        if(*src >= 'A' && *src <= 'Z')
        {
            *dest = *src + 32;   
        }
        else
        {
            *dest = *src;        
        }
        src++;
        dest++;
    }
    *dest = '\0';                
}
