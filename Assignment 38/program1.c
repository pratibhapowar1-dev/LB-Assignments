void StrCpyRev(char *src, char*dest)
{
    char *s = src;
    char *d = dest;
    
    while(*s != '\0')
    {
        s++;
    }
    s--;

    while(s >= src)
    {
       *d = *s;
       s--;
       d++;
    }
   *d ='\0';
}
