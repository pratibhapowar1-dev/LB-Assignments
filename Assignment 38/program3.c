void StrCpyX(char *src, char*dest)
{
   while(*src != '\0')
   {
    if (*src <= 'a' && *src >='z')
    {
        *dest = *src - 32;
    }
    else
    {
        *dest = *src;
    }
   }
   dest++;
   src++;

   *dest = '\0';
}
